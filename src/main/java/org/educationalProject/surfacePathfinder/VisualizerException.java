package org.educationalProject.surfacePathfinder;
/**
 * Exception class for Visualizer
 * */
public class VisualizerException extends Exception{
	 public VisualizerException() { super(); }
	 public VisualizerException(String message) { super(message); }
	 public VisualizerException(String message, Throwable cause) { super(message, cause); }
	 public VisualizerException(Throwable cause) { super(cause); }
}
